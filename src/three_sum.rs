    fn do_test_and_assert(input: Vec<i32>, expected_num_results: usize) {
        let mut results = Solution::three_sum(input);
        results.iter_mut().for_each(|triplet| {
            assert_eq!(0, triplet.iter().sum::<i32>());
            triplet.sort_unstable();
        });
        results.sort_unstable();
        results.dedup();
        assert_eq!(results.len(), expected_num_results);
    }

