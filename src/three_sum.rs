//! 15. 3Sum
//! https://leetcode.com/problems/3sum

use std::collections::{HashMap, HashSet};

#[derive(Debug, PartialEq, Eq, Hash)]
struct Triplet(i32, i32, i32);

impl Triplet {
    fn new(a: i32, b: i32, c: i32) -> Self {
        let mut v0;
        let mut v1;
        let v2;

        if a < b {
            v0 = a;
            v1 = b;
        } else {
            v0 = b;
            v1 = a;
        }

        if c < v0 {
            v2 = v1;
            v1 = v0;
            v0 = c;
        } else if c < v1 {
            v2 = v1;
            v1 = c;
        } else {
            v2 = c;
        }

        Self(v0, v1, v2)
    }
}

impl From<Triplet> for Vec<i32> {
    fn from(value: Triplet) -> Self {
        vec![value.0, value.1, value.2]
    }
}

impl Triplet {
    fn count(&self, value: i32) -> usize {
        let mut result = 0;

        if self.0 == value {
            result += 1;
        }

        if self.1 == value {
            result += 1;
        }

        if self.2 == value {
            result += 1;
        }

        result
    }
}

impl Solution {
    pub fn three_sum(nums: Vec<i32>) -> Vec<Vec<i32>> {
        let memo = {
            let mut memo: HashMap<i32, usize> = HashMap::new();

            for num in nums {
                memo.entry(num).and_modify(|v| *v += 1).or_insert(1);
            }

            memo
        };

        let mut result: HashSet<Triplet> = HashSet::new();
        for (&num0, &count0) in memo.iter() {
            for (&num1, &count1) in memo.iter() {
                let num2 = -num0 - num1;
                if let Some(&count2) = memo.get(&num2) {
                    let triplet = Triplet::new(num0, num1, num2);

                    if count0 < triplet.count(num0)
                        || count1 < triplet.count(num1)
                        || count2 < triplet.count(num2)
                    {
                        continue;
                    }

                    result.insert(triplet);
                }
            }
        }

        result.into_iter().map(Vec::<i32>::from).collect()
    }
}

