//! 15. 3Sum
//! https://leetcode.com/problems/3sum

use std::collections::{HashMap, HashSet};

#[derive(Debug, PartialEq, Eq, Hash)]
struct Triplet(i32, i32, i32);

impl Triplet {
    fn new(a: i32, b: i32, c: i32) -> Self {
        let mut v0;
        let mut v1;
        let v2;

        if a < b {
            v0 = a;
            v1 = b;
        } else {
            v0 = b;
            v1 = a;
        }

        if c < v0 {
            v2 = v1;
            v1 = v0;
            v0 = c;
        } else if c < v1 {
            v2 = v1;
            v1 = c;
        } else {
            v2 = c;
        }

        Self(v0, v1, v2)
    }
}

impl From<Triplet> for Vec<i32> {
    fn from(value: Triplet) -> Self {
        vec![value.0, value.1, value.2]
    }
}

impl Solution {
    pub fn three_sum(nums: Vec<i32>) -> Vec<Vec<i32>> {
        let memo = {
            let mut memo: HashMap<i32, Vec<usize>> = HashMap::new();

            for (index, &num) in nums.iter().enumerate() {
                memo.entry(num)
                    .and_modify(|v| v.push(index))
                    .or_insert_with(|| vec![index]);
            }

            memo
        };

        println!("{:?}", memo);

        let mut result: HashSet<Triplet> = HashSet::new();
        for (index0, &num0) in nums.iter().enumerate() {
            for (index1, &num1) in nums.iter().enumerate() {
                if index0 == index1 {
                    continue;
                }

                // num0 + num1 + num2 = 0 is the requirement
                // so search for a num2 = -num0 - num1
                if let Some(indices) = memo.get(&(-num0 - num1)) {
                    for &index2 in indices {
                        if index2 == index0 || index2 == index1 {
                            continue;
                        }

                        result.insert(Triplet::new(num0, num1, nums[index2]));
                    }
                }
            }
        }

        result.into_iter().map(Vec::<i32>::from).collect()
    }
}

